#include <unistd.h>

char		*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] += 32;
		}
		if (str[i] == '\0')
		{
			break ;
		}
		i++;
	}
	return (str);
}

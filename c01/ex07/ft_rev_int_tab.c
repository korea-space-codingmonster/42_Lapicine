#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int swap;
	int i;
	int ri;

	i = 0;
	ri = size - 1;
	while (i < (size / 2))
	{
		swap = tab[i];
		tab[i] = tab[ri];
		tab[ri] = swap;
		i++;
		ri--;
	}
}
